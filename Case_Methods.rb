#capitalize capitalizes first character

puts "hello".capitalize
puts "heLLO".capitalize
puts "HELLO".capitalize
puts "HELLO".capitalize.class

puts

# upcase method capitalizes everything


puts "hello".upcase
puts "heLLO".upcase
puts "HELLO".upcase
puts "HELLO".upcase.class

puts

#downcase
p "Ewa".downcase #everything lower case

puts

#swapcase
p "barczyKOWSKA".swapcase
